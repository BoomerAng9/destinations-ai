// Destinations AI — Export Module
// Google Drive (Doc, Sheet), PDF, Email dispatch

import type { ExportOptions } from './types';

/** Export HTML content as a Google Doc via Drive API */
export async function exportToGoogleDoc(
  title: string,
  htmlContent: string,
  accessToken: string
): Promise<{ id: string; webViewLink: string }> {
  const metadata = {
    name: title,
    mimeType: 'application/vnd.google-apps.document',
  };

  const form = new FormData();
  form.append(
    'metadata',
    new Blob([JSON.stringify(metadata)], { type: 'application/json' })
  );
  form.append('file', new Blob([htmlContent], { type: 'text/html' }));

  const res = await fetch(
    'https://www.googleapis.com/upload/drive/v3/files?uploadType=multipart&fields=id,webViewLink',
    {
      method: 'POST',
      headers: { Authorization: `Bearer ${accessToken}` },
      body: form,
    }
  );

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Google Doc export failed: ${err}`);
  }

  return res.json();
}

